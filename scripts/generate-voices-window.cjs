#!/usr/bin/env node
// Regenerates app/lib/voices-window.js from app/lib/voices.json.
// Same pattern as scripts/generate-tokens-css.cjs: JSON source -> JS
// window-global consumed by renderer.js. The R1.7 regression test in
// run-tests.cjs asserts byte-for-byte parity between the JSON source
// and the generated JS, so out-of-date checkouts fail CI.

const fs   = require('fs');
const path = require('path');

const ROOT   = path.resolve(__dirname, '..');
const SRC    = path.join(ROOT, 'app', 'lib', 'voices.json');
const OUT_JS = path.join(ROOT, 'app', 'lib', 'voices-window.js');

const voices = JSON.parse(fs.readFileSync(SRC, 'utf8'));

const js =
`// GENERATED by scripts/generate-voices-window.cjs from app/lib/voices.json.
// Do not hand-edit. Loaded by app/index.html before renderer.js.
window.TT_VOICES = Object.freeze(${JSON.stringify(voices, null, 2)});
`;
fs.writeFileSync(OUT_JS, js);

console.log(`voices generated: ${path.relative(ROOT, OUT_JS)}`);
